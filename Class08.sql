GO

--DATEDIFF FOR DIFFERENCE
SELECT DATEDIFF(DAY,'2023-07-05','2024-07-04') AS DayInYear
SELECT DATEDIFF(MONTH,'2023-07-05','2024-07-04') AS DayInYear
SELECT DATEDIFF(YEAR,'2023-07-05','2024-07-04') AS DayInYear
GO







